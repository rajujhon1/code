package code;

public class DemoDouble {
	
	public double Add(double doublenumber_one ,double doublenumber_two) {
		return doublenumber_one + doublenumber_two;
	}
	
	public double Sub(double doublenumber_one ,double  doublenumber_two) {
		return doublenumber_one - doublenumber_two;
	}
	
	public double Multi(double doublenumber_one ,double  doublenumber_two) {
		return doublenumber_one * doublenumber_two;
	}
	
	public double Div(double doublenumber_one ,double  doublenumber_two) {
		return doublenumber_one / doublenumber_two;
	}
	
	public double Modul(double doublenumber_one ,double doublenumber_two) {
		return doublenumber_one % doublenumber_two;


}
}
