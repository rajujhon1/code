package code;

public class Demolong {
	public long Add(long number_one ,long number_two) {
		return number_one + number_two;
	}
	
	public long Sub(long number_one ,long  number_two) {
		return number_one - number_two;
	}
	
	public long Mul(long number_one ,long  number_two) {
		return number_one * number_two;
	}
	
	public long Div(long number_one ,long  number_two) {
		return number_one / number_two;
	}
	
	public long Modul(long number_one ,long number_two) {
		return number_one % number_two;
	}

}
